# Генерирует агентские .md-файлы из общих шаблонов подстановкой плейсхолдеров {{...}}:
#   agents/coder.template.md    -> agents/coder.md, agents/coder_fast.md, agents/coder_deep.md
#   agents/reviewer.template.md -> agents/reviewer.md, agents/reviewer_std.md
# Шаблоны и сгенерированные варианты лежат в каталоге agents/; сам скрипт — в корне
# репозитория. Запускать после правки любого шаблона (или параметра варианта ниже) — все
# выходные файлы соответствующего семейства перезаписываются целиком.
#
# Coder-семейство: тела трёх файлов идентичны, различается только фронтматтер
# (name/model/effort/description). Reviewer-семейство: расходится и тело — вводный
# абзац идентичности в «Роли», блок fast-path/базового протокола в «Фазе 2» и суффикс
# «Агент:» в статусе; все эти точки параметризованы плейсхолдерами шаблона, а не
# правятся вручную после генерации (иначе reviewer.md/reviewer_std.md пришлось бы
# синхронизировать руками при каждом изменении контракта ревьюера).
#
# Порядок повторного запуска после правки: отредактируй нужный *.template.md (или
# значение параметра варианта ниже) -> запусти generate-coders.cmd (pwsh/powershell)
# -> закоммить перегенерированные *.md. Генерация детерминирована и идемпотентна:
# повторный прогон без изменений шаблона/параметров не меняет байты выходных файлов
# (переводы строк принудительно нормализуются к LF — стандарту репозитория).

$ErrorActionPreference = "Stop"
$root = Split-Path -Parent $MyInvocation.MyCommand.Path
# Templates and their generated variants live under agents/ (this script itself stays in
# the repo root). Read the *.template.md from there and write the generated *.md back to
# the same folder.
$agentsDir = Join-Path $root "agents"

$templates = @{
    coder    = Get-Content -Raw -Encoding UTF8 (Join-Path $agentsDir "coder.template.md")
    reviewer = Get-Content -Raw -Encoding UTF8 (Join-Path $agentsDir "reviewer.template.md")
}

# --- Тексты, расходящиеся между reviewer.md и reviewer_std.md (тело, не фронтматтер).
# Держим их здесь (а не в шаблоне), чтобы шаблон был один общий. Здесь-строки @'...'@
# литеральные: обратные кавычки и $ внутри не интерпретируются.

$reviewerIdentity = @'
Ты — независимый ревьюер, отвечающий за изменения по **одной задаче** `<T-ID>`
(её ID передаёт processor). В отличие от **full_reviewer** (интеграционное ревью на
джойн-барьере), ты проверяешь изменения одной задачи. Ты используешься для задач
уровня `coder`/`coder_deep`; для `coder_fast` processor вызывает **reviewer_std** —
твой более дешёвый аналог (sonnet/high) с теми же правилами.
'@

$reviewerStdIdentity = @'
Ты — независимый ревьюер для задач **уровня `coder_fast`** (простые, низкой
ответственности — так их оценил planner). Ты полностью аналогичен **reviewer**
(opus/high, используется для `coder`/`coder_deep`), кроме одного смягчения: на
первом цикле тебе позволено ограничиться **одним** прогоном вместо двух, если diff
мал и этот единственный прогон чист (см. «Режимы работы» ниже).
'@

$reviewerFastpath = @'
**Fast-path одного прогона** (только уровень `coder` — поле `Рекомендуемый исполнитель:`
в `task.md`): если diff **локальный и небольшой** (точечное изменение; растёкся по
нескольким модулям или превысил ориентировочно ~150 изменённых строк — это сигнал, что
оценка сложности была оптимистичной, применяй базовый протокол ниже), **первый** прогон
**чист** и открытых записей нет — можно закрыть **одним** прогоном (пер-таск ревью не
дублирует интеграционное; для чистого локального `coder`-изменения второй полный проход
избыточен, а межзадачный срез всё равно проверит full_reviewer, CI — финальный бэкстоп).

**Базовый протокол** (уровень `coder_deep`, **или** крупный/многомодульный diff, **или**
первый прогон нашёл проблемы): минимум `REVIEW_MIN_PASSES` прогонов; со следующего — если
**оба** последних прогона нашли новое, ещё прогон, иначе Фаза 3; после 5 прогонов подряд
с новыми — пометка `⚠️ После 5 прогонов проблемы не сходятся — нужна ручная проверка` и
стоп. Fast-path — оптимизация для действительно простых случаев, не обязанность срезать
угол: сомневаешься — делай базовый протокол.
'@


# --- Общий хвост Description для трёх coder-вариантов (тела идентичны, расходится
# только первое предложение — уровень исполнителя и модель). Держим хвост здесь одной
# константой, чтобы правка общего контракта описания не требовала трёх согласованных
# правок в $variants ниже.
$coderDescTail = 'Пишет код по уже спланированной задаче в .work/tasks/<T-ID>/task.md и самопроверяется; по запросу processor устраняет переданные ему находки ревью (R- по задаче или F- интеграционные) либо чинит точечный CI-фикс. Не гоняет ревью, не коммитит, не трогает очередь — цикл ревью, коммит, слияние и учёт ведёт processor. Один из трёх уровней исполнителей: coder_fast (простые) / coder (стандартные) / coder_deep (сложные, ответственные).'

$reviewerStdFastpath = @'
**Fast-path одного прогона** (уровень `coder_fast` — единственный, для которого тебя
вызывают): если diff **локальный и небольшой** (точечное изменение; растёкся по
нескольким модулям или превысил ориентировочно ~100 изменённых строк — это сигнал, что
оценка уровня была оптимистичной, применяй базовый протокол ниже), **первый** прогон
**чист** и открытых записей нет — можно закрыть **одним** прогоном (пер-таск ревью не
дублирует интеграционное; для чистого локального `coder_fast`-изменения второй полный
проход избыточен, а межзадачный срез всё равно проверит full_reviewer, CI — финальный
бэкстоп).

**Базовый протокол** (крупный/многомодульный diff **или** первый прогон нашёл проблемы):
минимум `REVIEW_MIN_PASSES` прогонов (по умолчанию 2, значение передаёт processor); со
следующего — если **оба** последних прогона нашли новое, ещё прогон, иначе Фаза 3; после
5 прогонов подряд с новыми — пометка `⚠️ После 5 прогонов проблемы не сходятся — нужна
ручная проверка` и стоп. Fast-path — оптимизация для действительно простых случаев, не
обязанность срезать угол: сомневаешься — делай базовый протокол.
'@

# Каждый вариант: из какого шаблона, имя выходного файла и таблица подстановок
# {{KEY}} -> Value. Значения не содержат последовательностей {{...}}, поэтому порядок
# подстановки на результат не влияет.
$variants = @(
    @{
        Template = 'coder'
        File     = 'coder_fast.md'
        Tokens   = [ordered]@{
            NAME        = 'coder_fast'
            MODEL       = 'sonnet'
            EFFORT      = 'medium'
            DESCRIPTION = "Быстрый листовой исполнитель простых задач низкой ответственности (sonnet/medium). $coderDescTail"
        }
    },
    @{
        Template = 'coder'
        File     = 'coder.md'
        Tokens   = [ordered]@{
            NAME        = 'coder'
            MODEL       = 'sonnet'
            EFFORT      = 'high'
            DESCRIPTION = "Стандартный листовой исполнитель (sonnet/high). $coderDescTail"
        }
    },
    @{
        Template = 'coder'
        File     = 'coder_deep.md'
        Tokens   = [ordered]@{
            NAME        = 'coder_deep'
            MODEL       = 'opus'
            EFFORT      = 'xhigh'
            DESCRIPTION = "Листовой исполнитель сложных, архитектурных и высокоответственных задач (opus/xhigh). $coderDescTail"
        }
    },
    @{
        Template = 'reviewer'
        File     = 'reviewer.md'
        Tokens   = [ordered]@{
            NAME        = 'reviewer'
            MODEL       = 'opus'
            MAXTURNS    = '80'
            DESCRIPTION = 'Проводит независимое ревью изменений по одной задаче уровня coder/coder_deep (opus/high). Читает дескриптор .work/tasks/<T-ID>/task.md, ведёт находки в .work/tasks/<T-ID>/review.md (записи R-NN, локальные для этой задачи), перепроверяет ранее исправленные/отклонённые. На первом цикле — минимум REVIEW_MIN_PASSES прогонов; исключение (fast-path): уровень coder с малым локальным чистым diff можно закрыть одним прогоном (coder_deep и крупные diff — всегда минимум 2). Дальше — режим повторного ревью по диапазону изменений. Не исправляет код. Вызывается processor по завершении реализации задачи или после цикла фиксов. Для задач уровня coder_fast используется более дешёвый reviewer_std.'
            IDENTITY    = $reviewerIdentity
            FASTPATH    = $reviewerFastpath
        }
    },
    @{
        Template = 'reviewer'
        File     = 'reviewer_std.md'
        Tokens   = [ordered]@{
            NAME        = 'reviewer_std'
            MODEL       = 'sonnet'
            MAXTURNS    = '60'
            DESCRIPTION = 'Проводит независимое ревью изменений по одной задаче уровня coder_fast (sonnet/high) — более дешёвый аналог reviewer для простых задач низкой ответственности. Читает дескриптор .work/tasks/<T-ID>/task.md, ведёт находки в .work/tasks/<T-ID>/review.md (записи R-NN, локальные для этой задачи). На первом цикле допускает один прогон вместо двух, если diff мал и прогон чист. Не исправляет код. Вызывается processor вместо reviewer, когда `Рекомендуемый исполнитель` задачи — coder_fast.'
            IDENTITY    = $reviewerStdIdentity
            FASTPATH    = $reviewerStdFastpath
        }
    }
)

foreach ($v in $variants) {
    if (-not $templates.ContainsKey($v.Template)) {
        throw "Неизвестный шаблон '$($v.Template)' для $($v.File)"
    }
    $rawTemplate = $templates[$v.Template]
    foreach ($key in $v.Tokens.Keys) {
        # Страховка от опечаток/удалённых плейсхолдеров в самом шаблоне: если ключ
        # из Tokens ни разу не встречается в сыром (ещё не заменённом) тексте
        # шаблона, подстановка молча ничего не находит и не заменяет — итоговый
        # файл получится неверным без единого следа {{...}} в выводе.
        if ($rawTemplate -notlike ('*{{' + $key + '}}*')) {
            throw "Плейсхолдер {{$key}} отсутствует в шаблоне '$($v.Template)' для $($v.File)"
        }
    }
    $out = $rawTemplate
    foreach ($key in $v.Tokens.Keys) {
        $out = $out.Replace('{{' + $key + '}}', [string]$v.Tokens[$key])
    }
    # Страховка от опечаток в наборе плейсхолдеров: незаменённый {{...}} => ошибка.
    $leftover = [regex]::Match($out, '\{\{[A-Za-z_]+\}\}')
    if ($leftover.Success) {
        throw "Незаменённый плейсхолдер $($leftover.Value) в $($v.File)"
    }
    # Нормализуем переводы строк к LF (стандарт репозитория) детерминированно, чтобы
    # байты выхода не зависели от того, какими переводами строк редактор сохранил
    # шаблон или встроенные значения. Для текущих LF-шаблонов это no-op.
    $out = $out.Replace("`r`n", "`n").Replace("`r", "`n")
    $outPath = Join-Path $agentsDir $v.File
    # Пишем UTF-8 БЕЗ BOM явно: Set-Content -Encoding UTF8 в Windows PowerShell 5.1
    # добавляет BOM, а BOM перед `---` frontmatter ломает парсинг агента.
    [System.IO.File]::WriteAllText($outPath, $out, (New-Object System.Text.UTF8Encoding($false)))
    Write-Host "Сгенерирован: $($v.File)"
}
